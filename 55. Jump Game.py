class Solution:
    def canJump(self, nums) -> bool:
        if len(nums) == 1:
            return True
        cache = {}
        def my_jump(i, cache):
            n = nums[i]
            if i not in cache:
                if n >= len(nums) - i-1:
                    cache[i] = True
                elif n == 0:
                    cache[i] = False
                else:
                    for r in [my_jump(i+j, cache) for j in range(n, 0, -1)]:
                        if r:
                            break
                    cache[i] = r
            print(i, n, cache[i])
            return cache[i]
        result = my_jump(0, cache)
        print(cache)
        return result

